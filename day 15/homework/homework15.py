def repeat_input_and_print(times=10):
    """სთხოვს მომხმარებელს შეყვანას და აბეჭდავს მის მიერ შეყვანილს times-ჯერ."""
    for n in range(times):
        value = input(f"Enter something ({n+1}/{times}): ")
        print("You entered:", value)


